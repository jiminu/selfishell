local plugin = require("config.plugin_versions").spec

return {
  plugin("hrsh7th/nvim-cmp", {
    event = "InsertEnter",
    dependencies = {
      plugin("hrsh7th/cmp-nvim-lsp"),
      plugin("hrsh7th/cmp-buffer"),
      plugin("hrsh7th/cmp-path"),
      plugin("L3MON4D3/LuaSnip"),
      plugin("saadparwaiz1/cmp_luasnip"),
    },
    config = function()
      local cmp = require("cmp")
      local luasnip = require("luasnip")

      cmp.setup({
        snippet = {
          expand = function(args)
            luasnip.lsp_expand(args.body)
          end,
        },

        -- Explicit mappings avoid behavior changes from preset updates.
        mapping = {
          ["<C-b>"] = cmp.mapping.scroll_docs(-4),
          ["<C-f>"] = cmp.mapping.scroll_docs(4),
          ["<C-Space>"] = cmp.mapping.complete(),

          -- Preserves the existing behavior: Enter accepts the first item
          -- even when it has not been explicitly selected.
          ["<CR>"] = cmp.mapping.confirm({ select = true }),

          ["<Tab>"] = cmp.mapping(function(fallback)
            if cmp.visible() then
              cmp.select_next_item()
            elseif luasnip.expand_or_jumpable() then
              luasnip.expand_or_jump()
            else
              fallback()
            end
          end, { "i", "s" }),

          ["<S-Tab>"] = cmp.mapping(function(fallback)
            if cmp.visible() then
              cmp.select_prev_item()
            elseif luasnip.jumpable(-1) then
              luasnip.jump(-1)
            else
              fallback()
            end
          end, { "i", "s" }),
        },

        sources = cmp.config.sources({
          { name = "nvim_lsp" },
          { name = "luasnip" },
        }, {
          { name = "buffer" },
          { name = "path" },
        }),
      })
    end,
  }),
}
